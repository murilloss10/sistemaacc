//for form1
var inputName1 = document.querySelector('label#customFileLang1')
var inputFicheiro1 = document.querySelector('input#customFileLang1')
inputFicheiro1.addEventListener('change', function() {
    var nameFile = 'Nenhum arquivo selecionado.'
    if(inputFicheiro1.files.length > 0) {
        nameFile = this.files[0].name
        inputName1.innerHTML = nameFile
        window.alert(nameFile + ' - Selecionado.')
    }else{
        inputName1.innerHTML = 'Nenhum arquivo selecionado.'
    }
});

